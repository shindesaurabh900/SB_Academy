    <h3 class="heading3">Introduction</h3>
    <strong>What is Python?</strong><br><br>
    Python is a popular programming language. It was created by Guido van Rossum, and released in 1991.<br>

    It is used for:<br>

    web development (server-side),<br>
    software development,<br>
    mathematics,<br>
    system scripting.<br><br>
    <strong></strong>What can Python do?</strong><br><br>
    Python can be used on a server to create web applications.<br>
    Python can be used alongside software to create workflows.<br>
    Python can connect to database systems. It can also read and modify files.<br>
    Python can be used to handle big data and perform complex mathematics.<br>
    Python can be used for rapid prototyping, or for production-ready software development.<br><br>
    <strong></strong>Why Python?</strong><br><br>
    Python works on different platforms (Windows, Mac, Linux, Raspberry Pi, etc).<br>
    Python has a simple syntax similar to the English language.<br>
    Python has syntax that allows developers to write programs with fewer lines than some other programming languages.<br>
    Python runs on an interpreter system, meaning that code can be executed as soon as it is written. This means that<br>
    prototyping can be very quick.<br>
    Python can be treated in a procedural way, an object-oriented way or a functional way.<br>
    Good to know<br>
    The most recent major version of Python is Python 3, which we shall be using in this tutorial. However, Python 2,<br>
    although not being updated with anything other than security updates, is still quite popular.<br>
    In this tutorial Python will be written in a text editor. It is possible to write Python in an Integrated<br>
    Development Environment, such as Thonny, Pycharm, Netbeans or Eclipse which are particularly useful when managing<br>
    larger collections of Python files.<br>
    Python Syntax compared to other programming languages<br>
    Python was designed for readability, and has some similarities to the English language with influence from<br>
    mathematics.<br>
    Python uses new lines to complete a command, as opposed to other programming languages which often use semicolons or<br>
    parentheses.<br>
    Python relies on indentation, using whitespace, to define scope; such as the scope of loops, functions and classes.<br>
    Other programming languages often use curly-brackets for this purpose.<br><br>
